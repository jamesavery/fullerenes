RSPIExample SymmetryExample(string sym, int number=0)
{
  int Nexamples = sizeof(symmetry_examples)/sizeof(RSPIExample), i = 0;
  string padded_sym = sym;
  padded_sym.insert(padded_sym.begin(), 3 - padded_sym.size(), ' ');
  
  for(i=0;i<Nexamples;i++)
    if(symmetry_examples[i].sym == padded_sym) break;

  if(i==Nexamples) { 
    fprintf(stderr,"Symmetry '%s' not found.\n",padded_sym.c_str()); abort(); 
  }

  RSPIExample e = symmetry_examples[i+number];
  if(e.sym != padded_sym) { 
    fprintf(stderr,"Requested example number %d exceeds number of %s-examples.\n",number,sym.c_str()); abort(); 
  }
  return e;
}
